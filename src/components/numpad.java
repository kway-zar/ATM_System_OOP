    private void initializeTilePanels() {
        
        
        this.setLayout(new GridLayout(4, 3, 5, 5)); 

        for (int i = 0; i < 12; i++) {
            final int tileIndex = i; 
                numButton[i] = new numpadButton();
                numButton[i].setButtonLabel(numpadLabel[i]);
                numButton[i].addMouseListener(new MouseAdapter() {
                @Override
                public void mouseClicked(MouseEvent evt) {
                    if (SwingUtilities.isLeftMouseButton(evt)) {
                        System.out.println(numButton[tileIndex].getLabelText());
                        if(numButton[tileIndex].getLabelText().equals("CLR")){
                            setTextString("");
                        
                        } else if(numButton[tileIndex].getLabelText().equals("<")){
                            if(getTextString().length() == 0){
                                setTextString("");
                            } else {
                                setTextString(getTextString().substring(0, getTextString().length() - 1));
                            }
                        } else {
                            if(getTextString().length() < 16){
                                setTextString(getTextString().concat(numpadLabel[tileIndex]));
                            }
                        }
                        System.out.println("Text:" + getTextString());
                            
                        
                        
                    }
                }
            });
            this.add(numButton[i]); 
        }
    }
